CODE SEGMENT  
ASSUME CS:CODE,DS:DATA,SS:STACK,ES:DATA
MAIN PROC FAR  
    
    MOV AX,DATA
	MOV DS,AX
	MOV SI,OFFSET BUFFER
	MOV CX,0
	MOV BX,0 
	MOV SI,0
	
	
CIRCLE_SELCTION:	
    CMP CX,20
	JE SHOW
    CMP BYTE PTR[SI],0
    JL  BELOW_ZEROS
    CMP BYTE PTR[SI],0
    JG  ABOVE_ZEROS  
    jmp  EQUAL_ZEROS
    
    
BELOW_ZEROS:
    INC SUM_BELOW_ZEROS 
    INC CX
    INC SI
    JMP CIRCLE_SELCTION
ABOVE_ZEROS:
    INC SUM_ABOVE_ZEROS          
    INC CX
    INC SI
    JMP CIRCLE_SELCTION
EQUAL_ZEROS:
    INC SUM_QEUAL_ZEROS
    INC CX
    INC SI
    JMP CIRCLE_SELCTION
         
SHOW:  
   MOV AH,09H
   LEA DX,NEGTIVE
   INT 21H
   MOV AL,SUM_BELOW_ZEROS
   CALL UBASC
   CALL PCHAR 
   
   MOV AH,09H
   LEA DX,POSTIVE
   INT 21H
   MOV AL,SUM_ABOVE_ZEROS
   CALL UBASC
   CALL PCHAR     
 
   MOV AH,09H
   LEA DX,ZEROS
   INT 21H
   MOV AL,SUM_QEUAL_ZEROS
   CALL UBASC
   CALL PCHAR
   
   JMP ENDD
 MAIN ENDP      
UBASC PROC
    AND AL,0FH
    ADD AL,90H 
    DAA
    ADC AL,40H
    DAA	
    RET 
UBASC ENDP 

PCHAR PROC
    MOV AH,02H
    MOV DL,AL
    INT 21H
    RET
PCHAR ENDP 


ENDD:
    MOV AH,4CH
    INT 21H
 
	
CODE	ENDS
	END	MAIN
	
	




   



  
