    DATA1  DW 81
    DATA2  DW 1
    RESULT DW 0
DATA ENDS
STACK SEGMENT 
    DB 256DUP(0)
STACK ENDS
CODE SEGMENT 
ASSUME DS:DATA,SS:STACK,CS:CODE,ES:DATA

MAIN PROC FAR
  MOV AX,DATA
  MOV DS,AX 
  MOV CX,0
  MOV AX,DATA1 
  MOV BX,DATA2
  
   
CIRCLE:
  SUB AX,BX 
  JAE M0 
  JB  M1
  
M0:
  INC CX
  INC BX
  INC BX
  JMP CIRCLE  
  
M1:
   MOV RESULT,CX
   JMP ENDD

ENDD:
    MOV AH,4CH
    INT 21H    

   
MAIN ENDP
CODE ENDS
     END MAIN
    
    

